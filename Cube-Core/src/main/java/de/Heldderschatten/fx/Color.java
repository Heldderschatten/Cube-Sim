package de.Heldderschatten.fx;

/**
 * Represents a set of predefined colors that can be used to describe the color
 * of objects such as sides or other cube-related elements.
 *
 * The available colors are:
 * - YELLOW
 * - RED
 * - GREEN
 * - BLUE
 * - WHITE
 * - ORANGE
 *
 * This enumeration is primarily used to assign a specific color to an instance
 * of a cube element (e.g., a side of a cube) and to facilitate operations that
 * require the identification or manipulation of colors.
 */
public enum Color {
    YELLOW,
    RED,
    GREEN,
    BLUE,
    WHITE,
    ORANGE;
}
